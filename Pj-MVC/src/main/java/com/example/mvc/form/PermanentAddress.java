package com.example.mvc.form;

public class PermanentAddress {
    
    private String city_town_village;
    private String houseNum;
    private String moo;
    private String road;
    private String subDistrict;
    private String district;
    private String province;
    private int postalCode;
    
    public String getCity_town_village() {
        return city_town_village;
    }
    public void setCity_town_village(String city_town_village) {
        this.city_town_village = city_town_village;
    }
    public String getHouseNum() {
        return houseNum;
    }
    public void setHouseNum(String houseNum) {
        this.houseNum = houseNum;
    }
    public String getMoo() {
        return moo;
    }
    public void setMoo(String moo) {
        this.moo = moo;
    }
    public String getRoad() {
        return road;
    }
    public void setRoad(String road) {
        this.road = road;
    }
    public String getSubDistrict() {
        return subDistrict;
    }
    public void setSubDistrict(String subDistrict) {
        this.subDistrict = subDistrict;
    }
    public String getDistrict() {
        return district;
    }
    public void setDistrict(String district) {
        this.district = district;
    }
    public String getProvince() {
        return province;
    }
    public void setProvince(String province) {
        this.province = province;
    }
    public int getPostalCode() {
        return postalCode;
    }
    public void setPostalCode(int postalCode) {
        this.postalCode = postalCode;
    }
   

}
